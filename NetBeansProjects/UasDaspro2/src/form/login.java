    private void logActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_logActionPerformed
        if (pen.isSelected()) {
            try {
                conn = DriverManager.getConnection(Url, User, Pass);
                String sql = "select nim, password from data where nim like '%" + user.getText() + "%'";
                st = conn.prepareStatement(sql);
                rs = st.executeQuery(sql);
                if (rs.next()) {
                    String db_pass = rs.getString("password");
                    String ent_pass = String.valueOf(passworda.getPassword());
                    if (ent_pass.equals(db_pass)) {
                        output.setText("Selamat Datang");

                    } else {
                        output.setText("Password salah");
                    }
                } else {
                    output.setText("Data tidak ada");
                }
            } catch (Exception x) {
                x.printStackTrace();
                JOptionPane.showMessageDialog(null, "Data tidak boleh kosong");
            }
        }
    }//GEN-LAST:event_logActionPerformed

