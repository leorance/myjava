package data_class;
public class data_pinjam {
    int nomor_urut, uang;
    String nama, no_telp, tgl;

    public int getNomor_urut() {
        return nomor_urut;
    }

    public void setNomor_urut(int nomor_urut) {
        this.nomor_urut = nomor_urut;
    }

    public int getUang() {
        return uang;
    }

    public void setUang(int uang) {
        this.uang = uang;
    }

    public String getNama() {
        return nama;
    }

    public void setNama(String nama) {
        this.nama = nama;
    }

    public String getNo_telp() {
        return no_telp;
    }

    public void setNo_telp(String no_telp) {
        this.no_telp = no_telp;
    }

    public String getTgl() {
        return tgl;
    }

    public void setTgl(String tgl) {
        this.tgl = tgl;
    }
}