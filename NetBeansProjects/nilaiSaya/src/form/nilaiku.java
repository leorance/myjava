    private void tampilData() {
        //membuat table model
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("NIM");
        model.addColumn("Nama");
        model.addColumn("Nilai Tugas");
        model.addColumn("Nilai Kuis 1");
        model.addColumn("Nilai UTS");
        model.addColumn("Nilai Kuis 2");
        model.addColumn("Nilai Tubes");
        model.addColumn("Nilai UAS");
        tblmahasiswa.setModel(model);

//        menampilkan database kedalam table
        try {
            java.sql.Statement stat = conn.createStatement();
            ResultSet data = stat.executeQuery("Select * from nama_table");
            while (data.next()) {
                model.addRow(new Object[]{
                    data.getString("row"),
                    data.getString("sesuai"),
                    data.getString("database")
                });
                tblmahasiswa.setModel(model);
            }
        } catch (Exception e) {
            System.err.println("terjadi kesalahan : " + e);
        }
    }
