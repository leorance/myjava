    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
      int y = Integer.parseInt(hasil.getText());
      int value = 0;
        if (op.equals("*")) {
            value = kali(xx,y);
        } else if (op.equals("/")) {
            value = bagi(xx,y);
        } if (op.equals("+")) {
            value = tambah(xx,y);
        } if (op.equals("-")) {
            value = kurang(xx,y);
        } if (op.equals("pangkat")) {
            value = (int) pangkat(xx,y);
        }
        hasil.setText(value + "");
    }//GEN-LAST:event_jButton1ActionPerformed

