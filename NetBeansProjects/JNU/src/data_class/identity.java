package data_class;

/**
 *
 * @author leorence
 */
public class identity {
    int id_penerima;
    String nama, provinsi, kotkab, kec, kel, kp, street, notelp;

    public int getId_penerima() {
        return id_penerima;
    }

    public void setId_penerima(int id_penerima) {
        this.id_penerima = id_penerima;
    }

    public String getNama() {
        return nama;
    }

    public void setNama(String nama) {
        this.nama = nama;
    }

    public String getProvinsi() {
        return provinsi;
    }

    public void setProvinsi(String provinsi) {
        this.provinsi = provinsi;
    }

    public String getKotkab() {
        return kotkab;
    }

    public void setKotkab(String kotkab) {
        this.kotkab = kotkab;
    }

    public String getKec() {
        return kec;
    }

    public void setKec(String kec) {
        this.kec = kec;
    }

    public String getKel() {
        return kel;
    }

    public void setKel(String kel) {
        this.kel = kel;
    }

    public String getKp() {
        return kp;
    }

    public void setKp(String kp) {
        this.kp = kp;
    }

    public String getStreet() {
        return street;
    }

    public void setStreet(String street) {
        this.street = street;
    }

    public String getNotelp() {
        return notelp;
    }

    public void setNotelp(String notelp) {
        this.notelp = notelp;
    }
}
