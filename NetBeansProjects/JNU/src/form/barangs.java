    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (blnn.getSelectedItem().equals("Januari")) {
            buls = 1;
        } else if (blnn.getSelectedItem().equals("Februari")) {
          buls = 2;  
        } else if (blnn.getSelectedItem().equals("Maret")) {
            buls = 3;
        } else if (blnn.getSelectedItem().equals("April")) {
            buls = 4;
        } else if (blnn.getSelectedItem().equals("Mei")) {
            buls = 5;
        } else if (blnn.getSelectedItem().equals("Juni")) {
            buls = 6;
        } else if (blnn.getSelectedItem().equals("Juli")) {
            buls = 7;
        } else if (blnn.getSelectedItem().equals("Agustus")) {
            buls = 8;
        } else if (blnn.getSelectedItem().equals("September")) {
            buls = 9;
        } else if (blnn.getSelectedItem().equals("Oktober")) {
            buls = 10;
        } else if (blnn.getSelectedItem().equals("November")) {
            buls = 11;
        } else if (blnn.getSelectedItem().equals("Desember")) {
            buls = 12;
        } else {
            System.out.println("--");
        }
        System.out.println(buls);
        
        barang x = new barang();
        String idp = String.valueOf(idpen.getSelectedItem());
        int idb = Integer.parseInt(idbar.getText());
        String nb = namabar.getText();
        int nore = Integer.parseInt(nores.getText());
        int bebe = Integer.parseInt(bb.getText());
        String jepe = null;
        if (y.isSelected()) {
            jepe = y.getText();
        } else if (r.isSelected()) {
            jepe = r.getText();
        } else if (e.isSelected()) {
            jepe = e.getText();
        }
        String jebe  = jb.getSelectedItem().toString();
        String tg = tgl.getSelectedItem().toString();
        int bl = buls;
        String th = thn.getSelectedItem().toString();
        if (idpen.getSelectedIndex() == 0) {
            JOptionPane.showMessageDialog(null, "Tolong masukkan id penerima");
        } else{
            x.setId_penerima(Integer.parseInt(idp));
        }
        x.setId_barang(idb);
        x.setNamabarang(nb);
        x.setNoresi(nore);
        x.setBerat(bebe);
        x.setJenispengiriman(jepe);
        x.setJenisbarang(jebe);
        x.setTanggal(tg);
        x.setBulan(""+bl);
        x.setTahun(th);
        x.setTotal(total);
        boolean status = data_akses_brg.insertData(x);
        if (!status) {
            JOptionPane.showMessageDialog(null, "Data inserted successfully");
            IPP i = new IPP();
            i.setVisible(true);
            dispose();
        } else {
            JOptionPane.showMessageDialog(null, "Data not inserted");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

