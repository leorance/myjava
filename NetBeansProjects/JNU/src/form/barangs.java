    void penerima(){
        try {
            conn = DriverManager.getConnection(Url, User, Pass);
            st = conn.createStatement();
            String sql = "SELECT id_penerima FROM Penerima";
            rs = st.executeQuery(sql);
            while (rs.next()) {
                String data = rs.getString(1);
                idpen.addItem(data);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
