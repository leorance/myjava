    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        String inama = namaa.getText();
        String ijurusan = jurusana.getText();
        String inim = nima.getText();
        String ipassword = String.valueOf(passworda.getPassword());
        String iemail = emaila.getText();
        String ihp = hpa.getText();
        String iline = linea.getText();

        String query = "INSERT INTO `data`(`nama`, `jurusan`, `nim`, `password`, `email`, `hp`, `line`) VALUES (?,?,?,?,?,?,?)";
        try {
            conn = DriverManager.getConnection(Url, User, Pass);
            ps = conn.prepareStatement(query);
            ps.setString(1, inama);
            ps.setString(2, ijurusan);
            ps.setString(3, inim);
            ps.setString(4, ipassword);
            ps.setString(5, iemail);
            ps.setString(6, ihp);
            ps.setString(7, iline);
            if (inama.equals("") || ijurusan.equals("") || inim.equals("") || ipassword.equals("") || iemail.equals("") || ihp.equals("") || iline.equals("")) {
                JOptionPane.showMessageDialog(null, "Data tidak boleh ada yang kosong");
            } else {
                ps.executeUpdate();
                JOptionPane.showMessageDialog(null, "Data berhasil di masukkan");
                ps.executeUpdate();
                namaa.setText("");
                jurusana.setText("");
                nima.setText("");
                passworda.setText("");
                emaila.setText("");
                hpa.setText("");
                linea.setText("");
            }
        } catch (SQLException ex) {
            java.util.logging.Logger.getLogger(registrasi.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, "Data gagal di masukkan, NIM hanya boleh 7 angka");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

